#1. Numeric Types
#a. int – Integer
product_quantity = 3
order_id = 10987432

#b. float – Floating-point
product_price = 749.99
discount_percentage = 15.5
average_rating = 4.3

#c. complex – Complex Numbers
z = 5 + 2j # Not commonly used in typical e-commerce workflows

#2. Sequence Types
#a. str – String
customer_name = "Rohit Sharma"
delivery_address = "Bangalore, Karnataka"
product_category = "Electronics"

#b. list – List
cart_items = ["Shoes", "T-shirt", "Headphones"]
top_categories = ["Mobiles", "Fashion", "Home", "Beauty"]

#c. tuple – Tuple
warehouse_location = (12.9716, 77.5946) # Latitude and Longitude
product_dimensions = (12.5, 9.8, 2.2) # Length, Width, Height in inches

#3. Set Types
#a. set – Set
available_sizes = {"S", "M", "L", "XL"}
popular_brands = {"Nike", "Adidas", "Puma", "Nike"} # 'Nike' only once

#b. frozenset – Immutable Set
frozen_tags = frozenset(["Sale", "Limited Edition", "Trending"])

#4. Mapping Type
#a. dict – Dictionary
product_details = {
"name": "Wireless Mouse",
"brand": "Logitech",
"price": 899.99,
"rating": 4.5
}
customer_profile = {
"name": "Meghana",
"email": "Meghana@example.com",
"prime_member": True
}

#5. Boolean Type
#a. bool – Boolean
is_logged_in = True
is_payment_successful = False
is_in_stock = True

#6. None Type
#a. NoneType – Represents a null or missing value
tracking_number = None
delivery_partner = None